def _contains_primitive(prim: Primitive, point: Sequence[float]) -> bool:
    local = transform_point(invert(prim.matrix), point)
    x, y, z = local
    p = prim.params
    if prim.kind == "cube":
        sx, sy, sz = p["sx"], p["sy"], p["sz"]
        if p["center"]:
            lo = (-sx / 2.0, -sy / 2.0, -sz / 2.0)
            hi = (sx / 2.0, sy / 2.0, sz / 2.0)
        else:
            lo = (0.0, 0.0, 0.0)
            hi = (sx, sy, sz)
        return (lo[0] - _EPS <= x <= hi[0] + _EPS and
                lo[1] - _EPS <= y <= hi[1] + _EPS and
                lo[2] - _EPS <= z <= hi[2] + _EPS)
    if prim.kind == "sphere":
        return x * x + y * y + z * z <= p["r"] ** 2 + _EPS
    if prim.kind == "cylinder":
        h = p["h"]
        z0 = -h / 2.0 if p["center"] else 0.0
        z1 = z0 + h
        if not (z0 - _EPS <= z <= z1 + _EPS):
            return False
        if h == 0:
            return False
        t = (z - z0) / h
        r = p["r1"] + (p["r2"] - p["r1"]) * t
        return x * x + y * y <= r * r + _EPS
    raise ScadEvalError("unknown primitive %r" % prim.kind)


def contains(node: Any, point: Sequence[float]) -> bool:
    """Exact point-membership classification of the CSG tree."""
    if node is None:
        return False
    if isinstance(node, Primitive):
        return _contains_primitive(node, point)
    if node.op == "union":
        return any(contains(c, point) for c in node.children)
    if node.op == "intersection":
        return bool(node.children) and all(contains(c, point) for c in node.children)
    if node.op == "difference":
        if not node.children:
            return False
        if not contains(node.children[0], point):
            return False
        return not any(contains(c, point) for c in node.children[1:])
    raise ScadEvalError("unknown boolean %r" % node.op)


def _primitive_corners(prim: Primitive) -> List[Vec3]:
    p = prim.params
    if prim.kind == "cube":
        sx, sy, sz = p["sx"], p["sy"], p["sz"]
        if p["center"]:
            lo = (-sx / 2.0, -sy / 2.0, -sz / 2.0)
            hi = (sx / 2.0, sy / 2.0, sz / 2.0)
        else:
            lo = (0.0, 0.0, 0.0)
            hi = (sx, sy, sz)
    elif prim.kind == "sphere":
        r = p["r"]
        lo, hi = (-r, -r, -r), (r, r, r)
    elif prim.kind == "cylinder":
        r = max(p["r1"], p["r2"])
        h = p["h"]
        z0 = -h / 2.0 if p["center"] else 0.0
        lo, hi = (-r, -r, z0), (r, r, z0 + h)
    else:
        raise ScadEvalError("unknown primitive %r" % prim.kind)
    corners = []
    for cx in (lo[0], hi[0]):
        for cy in (lo[1], hi[1]):
            for cz in (lo[2], hi[2]):
                corners.append(transform_point(prim.matrix, (cx, cy, cz)))
    return corners


def bounds(node: Any) -> Optional[Tuple[Vec3, Vec3]]:
    """Axis-aligned bounds ``(min, max)``; ``None`` for an empty tree.

    Exact for transformed primitive hulls; conservative for booleans
    (``difference`` keeps the first child's box, ``intersection`` intersects).
    """
    if node is None:
        return None
    if isinstance(node, Primitive):
        pts = _primitive_corners(node)
        lo = tuple(min(p[i] for p in pts) for i in range(3))
        hi = tuple(max(p[i] for p in pts) for i in range(3))
        return (lo, hi)
    boxes = [bounds(c) for c in node.children]
    boxes = [b for b in boxes if b is not None]
    if not boxes:
        return None
    if node.op == "union":
        lo = tuple(min(b[0][i] for b in boxes) for i in range(3))
        hi = tuple(max(b[1][i] for b in boxes) for i in range(3))
        return (lo, hi)
    if node.op == "difference":
        return boxes[0]
    if node.op == "intersection":
        lo = tuple(max(b[0][i] for b in boxes) for i in range(3))
        hi = tuple(min(b[1][i] for b in boxes) for i in range(3))
        if any(lo[i] > hi[i] for i in range(3)):
            return None
        return (lo, hi)
    raise ScadEvalError("unknown boolean %r" % node.op)


def voxelize(node: Any, resolution: int = 16,
             box: Optional[Tuple[Vec3, Vec3]] = None) -> List[List[List[bool]]]:
    """Occupancy grid on the cell-centre lattice of ``box`` (default: bounds).

    Deterministic: fixed lattice, no sampling. Index order ``grid[i][j][k]``
    over x, y, z.
    """
    if resolution < 1:
        raise ValueError("resolution must be >= 1")
    if box is None:
        box = bounds(node)
    if box is None:
        return []
    lo, hi = box
    step = [(hi[i] - lo[i]) / resolution for i in range(3)]
    grid: List[List[List[bool]]] = []
    for i in range(resolution):
        plane: List[List[bool]] = []
        x = lo[0] + (i + 0.5) * step[0]
        for j in range(resolution):
            row: List[bool] = []
            y = lo[1] + (j + 0.5) * step[1]
            for k in range(resolution):
                z = lo[2] + (k + 0.5) * step[2]
                row.append(contains(node, (x, y, z)))
            plane.append(row)
        grid.append(plane)
    return grid


def volume(node: Any, resolution: int = 16) -> float:
    """Volume estimate from the occupancy grid over the tree's bounds."""
    box = bounds(node)
    if box is None:
        return 0.0
    lo, hi = box
    cell = 1.0
    for i in range(3):
        cell *= (hi[i] - lo[i]) / resolution
    grid = voxelize(node, resolution, box)
    filled = sum(1 for plane in grid for row in plane for v in row if v)
    return filled * cell
