def _collect_defs(stmts: Sequence[Any], modules: Dict[str, ModuleDef],
                  functions: Dict[str, FunctionDef]) -> None:
    for s in stmts:
        if isinstance(s, ModuleDef):
            modules[s.name] = s
        elif isinstance(s, FunctionDef):
            functions[s.name] = s


def _eval_stmts(stmts: Sequence[Any], env: _Env, frame: _Frame,
                matrix: Matrix) -> List[Any]:
    _collect_defs(stmts, frame.modules, frame.functions)
    nodes: List[Any] = []
    for s in stmts:
        nodes.extend(_eval_stmt(s, env, frame, matrix))
    return nodes


def _eval_stmt(stmt: Any, env: _Env, frame: _Frame, matrix: Matrix) -> List[Any]:
    if isinstance(stmt, (NoOp, ModuleDef, FunctionDef, Include)):
        return []
    if isinstance(stmt, Assign):
        env.define(stmt.name, _eval_expr(stmt.value, env, frame))
        return []
    if isinstance(stmt, Block):
        return _eval_stmts(stmt.body, env.child(), frame, matrix)
    if isinstance(stmt, IfStmt):
        if _truthy(_eval_expr(stmt.cond, env, frame)):
            return _eval_stmt(stmt.then, env.child(), frame, matrix)
        if stmt.orelse is not None:
            return _eval_stmt(stmt.orelse, env.child(), frame, matrix)
        return []
    if isinstance(stmt, LetStmt):
        inner = env.child()
        for name, value in stmt.bindings:
            inner.define(name, _eval_expr(value, inner, frame))
        return _eval_stmt(stmt.body, inner, frame, matrix)
    if isinstance(stmt, ForStmt):
        out: List[Any] = []
        for combo in _binding_product(stmt.bindings, env, frame):
            inner = env.child()
            inner.vars.update(combo)
            out.extend(_eval_stmt(stmt.body, inner, frame, matrix))
        if stmt.intersect:
            return [BoolNode("intersection", out)] if out else []
        return out
    if isinstance(stmt, ModuleCall):
        return _eval_module_call(stmt, env, frame, matrix)
    raise ScadEvalError("cannot evaluate statement %s" % type(stmt).__name__)


def _args_of(call: ModuleCall, env: _Env, frame: _Frame):
    positional: List[Any] = []
    named: Dict[str, Any] = {}
    for a in call.args:
        value = _eval_expr(a.value, env, frame)
        if a.name is None:
            positional.append(value)
        else:
            named[a.name] = value
    return positional, named


def _pick(positional: List[Any], named: Dict[str, Any], index: int,
          *names: str) -> Any:
    for n in names:
        if n in named:
            return named[n]
    if index < len(positional):
        return positional[index]
    return None


def _eval_children(call: ModuleCall, env: _Env, frame: _Frame,
                   matrix: Matrix) -> List[Any]:
    child_frame = _Frame(frame.modules, frame.functions, frame.children,
                         frame.child_env, frame.child_matrix, frame.depth)
    return _eval_stmts(call.children, env.child(), child_frame, matrix)


def _eval_module_call(call: ModuleCall, env: _Env, frame: _Frame,
                      matrix: Matrix) -> List[Any]:
    name = call.name
    if call.modifier == "*":            # disabled subtree
        return []
    if name in UNSUPPORTED:
        raise ScadEvalError("unsupported construct %r (no mesh kernel)" % name)

    positional, named = _args_of(call, env, frame)

    if name in PRIMITIVES:
        return [_make_primitive(name, positional, named, matrix)]

    if name in BOOLEANS:
        children = _eval_children(call, env, frame, matrix)
        if not children:
            return []
        if name == "difference" and len(children) == 1:
            return [children[0]]
        if len(children) == 1 and name != "difference":
            return [children[0]]
        return [BoolNode(name, children)]

    if name in TRANSFORMS:
        local = _transform_matrix(name, positional, named)
        return _eval_children(call, env, frame, matmul(matrix, local))

    if name == "children":
        if not frame.children or frame.child_env is None:
            return []
        idx = positional[0] if positional else None
        selected = frame.children
        if idx is not None:
            if isinstance(idx, list):
                selected = [frame.children[int(_num(i))] for i in idx]
            else:
                selected = [frame.children[int(_num(idx))]]
        parent_frame = _Frame(frame.modules, frame.functions, depth=frame.depth)
        out: List[Any] = []
        for c in selected:
            out.extend(_eval_stmt(c, frame.child_env.child(), parent_frame, matrix))
        return out

    if name in frame.modules:
        return _call_user_module(frame.modules[name], call, positional, named,
                                 env, frame, matrix)

    raise ScadEvalError("unknown module %r" % name)


def _call_user_module(defn: ModuleDef, call: ModuleCall, positional, named,
                      env: _Env, frame: _Frame, matrix: Matrix) -> List[Any]:
    if frame.depth >= _MAX_DEPTH:
        raise ScadEvalError("recursion limit exceeded")
    inner = _Env(None)
    for p in defn.params:
        inner.define(p.name, _eval_expr(p.default, env, frame)
                     if p.default is not None else None)
    for i, value in enumerate(positional):
        if i >= len(defn.params):
            raise ScadEvalError("too many positional arguments to module %r"
                                % defn.name)
        inner.define(defn.params[i].name, value)
    known = {p.name for p in defn.params}
    for key, value in named.items():
        if key not in known:
            raise ScadEvalError("unknown argument %r to module %r" % (key, defn.name))
        inner.define(key, value)
    sub = _Frame(frame.modules, frame.functions, list(call.children), env,
                 matrix, frame.depth + 1)
    return _eval_stmt(defn.body, inner, sub, matrix)


def _transform_matrix(name: str, positional: List[Any],
                      named: Dict[str, Any]) -> Matrix:
    if name in ("color", "group", "render"):
        return identity()
    if name == "translate":
        v = _pick(positional, named, 0, "v")
        return _translation(_vec3(v if v is not None else [0, 0, 0]))
    if name == "scale":
        v = _pick(positional, named, 0, "v")
        return _scaling(_vec3(v if v is not None else [1, 1, 1], default=1.0))
    if name == "mirror":
        v = _pick(positional, named, 0, "v")
        return _mirror(_vec3(v if v is not None else [1, 0, 0]))
    if name == "rotate":
        a = _pick(positional, named, 0, "a")
        v = _pick(positional, named, 1, "v")
        if isinstance(a, list):
            return _rotation_xyz(_vec3(a))
        if a is None:
            return identity()
        if v is not None:
            return _rot_axis(_num(a, "rotate angle"), _vec3(v))
        return _rot_axis(_num(a, "rotate angle"), (0, 0, 1))
    if name == "multmatrix":
        m = _pick(positional, named, 0, "m")
        if not isinstance(m, list) or not m:
            raise ScadEvalError("multmatrix requires a matrix")
        rows = []
        for i in range(4):
            if i < len(m):
                row = [_num(x, "matrix entry") for x in m[i]]
            else:
                row = [0.0, 0.0, 0.0, 0.0]
            while len(row) < 4:
                row.append(1.0 if len(row) == i else 0.0)
            rows.append(tuple(row[:4]))
        while len(rows) < 4:
            rows.append((0.0, 0.0, 0.0, 1.0))
        return tuple(rows)
    if name == "resize":
        raise ScadEvalError("resize() requires a mesh kernel; unsupported")
    raise ScadEvalError("unknown transform %r" % name)


def _make_primitive(kind: str, positional: List[Any], named: Dict[str, Any],
                    matrix: Matrix) -> Primitive:
    if kind == "cube":
        size = _pick(positional, named, 0, "size")
        center = _truthy(_pick(positional, named, 1, "center"))
        sx, sy, sz = _vec3(size if size is not None else 1.0, default=1.0, what="cube size")
        params = {"sx": sx, "sy": sy, "sz": sz, "center": 1.0 if center else 0.0}
        return Primitive("cube", params, matrix)
    if kind == "sphere":
        r = _pick(positional, named, 0, "r")
        d = named.get("d")
        radius = _num(d, "sphere d") / 2.0 if d is not None else (
            _num(r, "sphere r") if r is not None else 1.0)
        return Primitive("sphere", {"r": radius}, matrix)
    if kind == "cylinder":
        h = _pick(positional, named, 0, "h")
        height = _num(h, "cylinder h") if h is not None else 1.0
        r = _pick(positional, named, 1, "r")
        d = named.get("d")
        r1 = named.get("r1")
        r2 = named.get("r2")
        d1 = named.get("d1")
        d2 = named.get("d2")
        if r1 is None and d1 is not None:
            r1 = _num(d1, "cylinder d1") / 2.0
        if r2 is None and d2 is not None:
            r2 = _num(d2, "cylinder d2") / 2.0
        if r1 is None and r2 is None and len(positional) >= 3 and "r" not in named:
            # cylinder(h, r1, r2) positional form
            r1, r2 = positional[1], positional[2]
        base = None
        if d is not None:
            base = _num(d, "cylinder d") / 2.0
        elif r is not None and r1 is None and r2 is None:
            base = _num(r, "cylinder r")
        if r1 is None:
            r1 = base if base is not None else 1.0
        if r2 is None:
            r2 = base if base is not None else 1.0
        center = _truthy(named.get("center", False))
        params = {"h": height, "r1": _num(r1, "cylinder r1"),
                  "r2": _num(r2, "cylinder r2"), "center": 1.0 if center else 0.0}
        return Primitive("cylinder", params, matrix)
    raise ScadEvalError("unknown primitive %r" % kind)


