def evaluate(stmts: Sequence[Any]) -> Any:
    """Evaluate a parsed OpenSCAD program into a CSG tree (or ``None`` if empty)."""
    frame = _Frame({}, {})
    env = _Env(None)
    nodes = _eval_stmts(stmts, env, frame, identity())
    if not nodes:
        return None
    if len(nodes) == 1:
        return nodes[0]
    return BoolNode("union", nodes)


def evaluate_source(source: str) -> Any:
    """Parse and evaluate OpenSCAD source into a CSG tree."""
    return evaluate(parse(source))


def flatten(node: Any, sign: int = 1) -> List[Tuple[Primitive, int]]:
    """Leaf primitives with their additive (+1) / subtractive (-1) sign.

    The sign is only exact for union/difference chains; primitives inside an
    ``intersection`` are reported as additive.
    """
    if node is None:
        return []
    if isinstance(node, Primitive):
        return [(node, sign)]
    out: List[Tuple[Primitive, int]] = []
    if node.op == "difference":
        for i, c in enumerate(node.children):
            out.extend(flatten(c, sign if i == 0 else -sign))
    else:
        for c in node.children:
            out.extend(flatten(c, sign))
    return out


