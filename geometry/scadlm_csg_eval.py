"""Deterministic OpenSCAD evaluator: source -> CSG tree -> geometry.

ScadLM's loop can only learn anything about the geometry it generated by
shelling out to the ``openscad`` binary (STL + PNG renders) and feeding the
picture back to a vision model. Everything geometric in that loop is external.

This module replaces the *deterministic* half of that binary locally: it takes
the AST from :mod:`programs.scadlm_ast` and **evaluates** it -- resolving
variables, user ``function``/``module`` definitions, ``for`` loops, ``if``,
``let``, ``children()``, and the OpenSCAD builtin function library -- into a
flat CSG tree of primitives, each carrying the 4x4 world transform accumulated
from the enclosing ``translate`` / ``rotate`` / ``scale`` / ``mirror`` /
``multmatrix`` chain, combined by ``union`` / ``difference`` / ``intersection``.

The resulting tree supports exact deterministic geometry queries with no mesh
kernel:

  * :func:`contains` -- point membership classification (inverse-transform the
    query point into each primitive's local frame; booleans compose exactly);
  * :func:`bounds` -- an axis-aligned bounding box (exact for the primitive
    hulls, conservative for booleans -- difference keeps the minuend's box);
  * :func:`voxelize` / :func:`volume` -- occupancy grid and volume estimate on
    a fixed grid (deterministic: a fixed cell-centre lattice, no sampling);
  * :func:`flatten` -- the (primitive, matrix, sign) leaf list, useful for
    diffing two programs' geometry.

Supported primitives: ``cube``, ``sphere``, ``cylinder`` (incl. cones via
``r1``/``r2``). Unsupported *geometry* (hull, minkowski, extrusions, imports,
polyhedron) raises :class:`ScadEvalError` rather than silently producing the
wrong solid -- an honest "cannot evaluate" is what makes the checker usable as a
verifier.

Pure stdlib, no ``exec``, no randomness, no clock: the same source always
yields the same tree and the same voxel grid.
"""

