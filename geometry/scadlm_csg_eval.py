# ---------------------------------------------------------------------------
# Environment
# ---------------------------------------------------------------------------

class _Env:
    def __init__(self, parent: Optional["_Env"] = None) -> None:
        self.vars: Dict[str, Any] = {}
        self.parent = parent

    def lookup(self, name: str) -> Any:
        env: Optional[_Env] = self
        while env is not None:
            if name in env.vars:
                return env.vars[name]
            env = env.parent
        return None  # OpenSCAD: unknown variable is undef

    def define(self, name: str, value: Any) -> None:
        self.vars[name] = value

    def child(self) -> "_Env":
        return _Env(self)


@dataclass
class _Frame:
    """Module/function tables plus the pending ``children()`` of a module call."""

    modules: Dict[str, ModuleDef]
    functions: Dict[str, FunctionDef]
    children: List[Any] = field(default_factory=list)
    child_env: Optional[_Env] = None
    child_matrix: Matrix = field(default_factory=identity)
    depth: int = 0


_MAX_DEPTH = 64


# ---------------------------------------------------------------------------
# Expression evaluation
# ---------------------------------------------------------------------------

def _eval_expr(node: Any, env: _Env, frame: _Frame) -> Any:
    if isinstance(node, Num):
        return node.value
    if isinstance(node, Str):
        return node.value
    if isinstance(node, Bool):
        return node.value
    if isinstance(node, Undef):
        return None
    if isinstance(node, Name):
        return env.lookup(node.ident)
    if isinstance(node, Vector):
        out: List[Any] = []
        for item in node.items:
            _eval_vector_item(item, env, frame, out)
        return out
    if isinstance(node, Range):
        start = _num(_eval_expr(node.start, env, frame), "range start")
        end = _num(_eval_expr(node.end, env, frame), "range end")
        step = 1.0 if node.step is None else _num(_eval_expr(node.step, env, frame),
                                                  "range step")
        return _range_values(start, step, end)
    if isinstance(node, Unary):
        v = _eval_expr(node.operand, env, frame)
        if node.op == "!":
            return not _truthy(v)
        if isinstance(v, list):
            return [-_num(x) for x in v]
        return -_num(v, "unary minus operand")
    if isinstance(node, Binary):
        return _eval_binary(node, env, frame)
    if isinstance(node, Ternary):
        cond = _truthy(_eval_expr(node.cond, env, frame))
        return _eval_expr(node.if_true if cond else node.if_false, env, frame)
    if isinstance(node, Index):
        target = _eval_expr(node.target, env, frame)
        idx = _eval_expr(node.index, env, frame)
        if not isinstance(target, (list, str)):
            return None
        i = int(_num(idx, "index"))
        if i < 0 or i >= len(target):
            return None
        return target[i]
    if isinstance(node, Member):
        target = _eval_expr(node.target, env, frame)
        pos = {"x": 0, "y": 1, "z": 2}.get(node.name)
        if pos is None or not isinstance(target, list) or pos >= len(target):
            return None
        return target[pos]
    if isinstance(node, LetExpr):
        inner = env.child()
        for name, value in node.bindings:
            inner.define(name, _eval_expr(value, inner, frame))
        return _eval_expr(node.body, inner, frame)
    if isinstance(node, FunctionLiteral):
        return node
    if isinstance(node, Call):
        return _eval_call(node, env, frame)
    raise ScadEvalError("cannot evaluate expression node %s" % type(node).__name__)


def _range_values(start: float, step: float, end: float) -> List[float]:
    if step == 0:
        raise ScadEvalError("range step must be non-zero")
    values: List[float] = []
    count = int(math.floor((end - start) / step + 1e-9)) + 1
    if count > 100000:
        raise ScadEvalError("range too large (%d elements)" % count)
    for i in range(max(count, 0)):
        values.append(start + i * step)
    return values


def _eval_vector_item(item: Any, env: _Env, frame: _Frame, out: List[Any]) -> None:
    if isinstance(item, Comprehension):
        if item.kind == "for":
            for combo in _binding_product(item.bindings, env, frame):
                inner = env.child()
                inner.vars.update(combo)
                _eval_vector_item(item.body, inner, frame, out)
            return
        if item.kind == "let":
            inner = env.child()
            for name, value in item.bindings:
                inner.define(name, _eval_expr(value, inner, frame))
            _eval_vector_item(item.body, inner, frame, out)
            return
        if item.kind == "if":
            if _truthy(_eval_expr(item.cond, env, frame)):
                _eval_vector_item(item.body, env, frame, out)
            elif item.orelse is not None:
                _eval_vector_item(item.orelse, env, frame, out)
            return
        if item.kind == "each":
            value = _eval_expr(item.body, env, frame) if not isinstance(
                item.body, Comprehension) else None
            if isinstance(item.body, Comprehension):
                sub: List[Any] = []
                _eval_vector_item(item.body, env, frame, sub)
                out.extend(sub)
                return
            if isinstance(value, list):
                out.extend(value)
            elif value is not None:
                out.append(value)
            return
        raise ScadEvalError("unknown comprehension kind %r" % item.kind)
    out.append(_eval_expr(item, env, frame))


def _binding_product(bindings, env: _Env, frame: _Frame):
    """Cartesian product of ``for`` bindings, in OpenSCAD's order."""
    combos: List[Dict[str, Any]] = [{}]
    for name, expr in bindings:
        values = _eval_expr(expr, env, frame)
        if not isinstance(values, list):
            values = [values]
        combos = [dict(c, **{name: v}) for c in combos for v in values]
    return combos


def _eval_binary(node: Binary, env: _Env, frame: _Frame) -> Any:
    op = node.op
    if op == "&&":
        return _truthy(_eval_expr(node.left, env, frame)) and \
            _truthy(_eval_expr(node.right, env, frame))
    if op == "||":
        return _truthy(_eval_expr(node.left, env, frame)) or \
            _truthy(_eval_expr(node.right, env, frame))
    a = _eval_expr(node.left, env, frame)
    b = _eval_expr(node.right, env, frame)
    if op == "==":
        return a == b
    if op == "!=":
        return a != b
    if op in ("<", "<=", ">", ">="):
        x, y = _num(a, "comparison"), _num(b, "comparison")
        return {"<": x < y, "<=": x <= y, ">": x > y, ">=": x >= y}[op]
    if op == "+":
        if isinstance(a, list) and isinstance(b, list):
            return [_num(x) + _num(y) for x, y in zip(a, b)]
        return _num(a, "'+'") + _num(b, "'+'")
    if op == "-":
        if isinstance(a, list) and isinstance(b, list):
            return [_num(x) - _num(y) for x, y in zip(a, b)]
        return _num(a, "'-'") - _num(b, "'-'")
    if op == "*":
        if isinstance(a, list) and isinstance(b, list):  # dot product
            return sum(_num(x) * _num(y) for x, y in zip(a, b))
        if isinstance(a, list):
            return [_num(x) * _num(b) for x in a]
        if isinstance(b, list):
            return [_num(a) * _num(y) for y in b]
        return _num(a, "'*'") * _num(b, "'*'")
    if op == "/":
        if isinstance(a, list):
            return [_num(x) / _num(b) for x in a]
        d = _num(b, "'/'")
        if d == 0:
            raise ScadEvalError("division by zero")
        return _num(a, "'/'") / d
    if op == "%":
        d = _num(b, "'%'")
        if d == 0:
            raise ScadEvalError("modulo by zero")
        return math.fmod(_num(a, "'%'"), d)
    if op == "^":
        return math.pow(_num(a, "'^'"), _num(b, "'^'"))
    raise ScadEvalError("unknown operator %r" % op)


def _eval_call(node: Call, env: _Env, frame: _Frame) -> Any:
    args = [(a.name, _eval_expr(a.value, env, frame)) for a in node.args]
    positional = [v for n, v in args if n is None]
    named = {n: v for n, v in args if n is not None}

    callee: Any = node.name
    if isinstance(callee, Name):
        name = callee.ident
        if name in frame.functions:
            fn = frame.functions[name]
            return _apply_function(fn.params, fn.body, positional, named, env, frame)
        value = env.lookup(name)
        if isinstance(value, FunctionLiteral):
            return _apply_function(value.params, value.body, positional, named,
                                   env, frame)
        if name in BUILTIN_FUNCTIONS:
            if named:
                raise ScadEvalError("builtin %s() takes no named arguments" % name)
            try:
                return BUILTIN_FUNCTIONS[name](*positional)
            except ScadEvalError:
                raise
            except Exception as exc:  # arity / domain errors
                raise ScadEvalError("error calling %s(): %s" % (name, exc))
        raise ScadEvalError("unknown function %r" % name)
    value = _eval_expr(callee, env, frame)
    if isinstance(value, FunctionLiteral):
        return _apply_function(value.params, value.body, positional, named, env, frame)
    raise ScadEvalError("expression is not callable")


def _apply_function(params, body, positional, named, env: _Env, frame: _Frame) -> Any:
    if frame.depth >= _MAX_DEPTH:
        raise ScadEvalError("recursion limit exceeded")
    inner = _Env(None)
    # defaults evaluated in the definition-time (global) scope approximation
    for p in params:
        inner.define(p.name, _eval_expr(p.default, env, frame)
                     if p.default is not None else None)
    for i, value in enumerate(positional):
        if i >= len(params):
            raise ScadEvalError("too many positional arguments")
        inner.define(params[i].name, value)
    known = {p.name for p in params}
    for key, value in named.items():
        if key not in known:
            raise ScadEvalError("unknown named argument %r" % key)
        inner.define(key, value)
    sub = _Frame(frame.modules, frame.functions, depth=frame.depth + 1)
    return _eval_expr(body, inner, sub)


