def _num(v: Any, what: str = "value") -> float:
    if isinstance(v, bool):
        return 1.0 if v else 0.0
    if isinstance(v, (int, float)):
        return float(v)
    raise ScadEvalError("expected a number for %s, got %r" % (what, v))


def _vec3(v: Any, default: float = 0.0, what: str = "vector") -> Vec3:
    if isinstance(v, (int, float)) and not isinstance(v, bool):
        return (float(v), float(v), float(v))
    if isinstance(v, list):
        items = [_num(x, what) for x in v[:3]]
        while len(items) < 3:
            items.append(default)
        return (items[0], items[1], items[2])
    raise ScadEvalError("expected a vector for %s, got %r" % (what, v))


def _truthy(v: Any) -> bool:
    if v is None:
        return False
    if isinstance(v, bool):
        return v
    if isinstance(v, (int, float)):
        return v != 0
    if isinstance(v, (str, list)):
        return len(v) > 0
    return True


def _deg(fn):
    return lambda x: fn(math.radians(_num(x)))


def _norm(v: Any) -> float:
    return math.sqrt(sum(_num(x) ** 2 for x in v))


def _minmax(fn):
    def call(*args):
        if len(args) == 1 and isinstance(args[0], list):
            args = tuple(args[0])
        return fn(_num(a) for a in args)
    return call


def _cross(a: Any, b: Any) -> List[float]:
    a3, b3 = _vec3(a), _vec3(b)
    return [a3[1] * b3[2] - a3[2] * b3[1],
            a3[2] * b3[0] - a3[0] * b3[2],
            a3[0] * b3[1] - a3[1] * b3[0]]


def _scad_str(*args: Any) -> str:
    out = []
    for a in args:
        if a is None:
            out.append("undef")
        elif isinstance(a, bool):
            out.append("true" if a else "false")
        elif isinstance(a, float):
            out.append(str(int(a)) if a == int(a) else str(a))
        else:
            out.append(str(a))
    return "".join(out)


