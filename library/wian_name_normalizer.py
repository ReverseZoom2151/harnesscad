"""User-provided CAD name hygiene: default-name detection and normalisation.

Derived from the data-preparation stage of "What's In A Name? Evaluating
Assembly-Part Semantic Knowledge in Language Models through User-Provided Names
in CAD Files" (Meltzer, Lambourne, Grandi, JCISE 2023), whose released dataset
is "the complete set of data extracted with default part names removed" and is
"deduplicated based on part names and feature names".

The published repository ships the *result* of that filtering, not the filter.
This module rebuilds it deterministically:

* ``is_default_name`` -- recognises the auto-generated names emitted by the
  major CAD hosts (Onshape, SolidWorks, Fusion 360, FreeCAD, Inventor, generic
  STEP/IGES importers), so that a corpus can keep only names a human actually
  typed.
* ``normalize_name`` / ``tokenize_name`` -- casefold, split snake_case,
  kebab-case, camelCase and digit boundaries, and drop the trailing instance
  index ("bracket_v2 <3>" -> ["bracket", "v2"]).
* ``clean_document`` / ``clean_corpus`` -- apply the filter + order-preserving
  dedup to a whole ``{document_id: {body_names, feature_names, ...}}`` corpus
  in the paper's schema.

Everything here is pure stdlib and deterministic.
"""

from __future__ import annotations

from dataclasses import dataclass, field
import re
from typing import Dict, Iterable, List, Mapping, Sequence, Tuple

