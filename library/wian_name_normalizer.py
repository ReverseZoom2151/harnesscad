_STEM_SET = frozenset(DEFAULT_STEMS)

# "<3>", "(2)", ":1", "^assembly" style instance suffixes appended by hosts.
_INSTANCE_SUFFIX = re.compile(r"(\s*<\s*\d+\s*>|\s*\(\s*\d+\s*\)|\s*:\s*\d+|\s*\^\S+)\s*$")

# trailing counter: "Extrude 12", "Extrude12", "Extrude_12", "Extrude-12"
_TRAILING_INDEX = re.compile(r"^(?P<stem>.*?)[\s_\-]*(?P<num>\d+)$")

_SEPARATORS = re.compile(r"[\s_\-/\\.,;:+&()\[\]{}<>\"'*#]+")
_CAMEL = re.compile(r"(?<=[a-z0-9])(?=[A-Z])|(?<=[A-Z])(?=[A-Z][a-z])")
_ALNUM_SPLIT = re.compile(r"(?<=[A-Za-z])(?=\d)|(?<=\d)(?=[A-Za-z])")
_WS = re.compile(r"\s+")


def strip_instance_suffix(name: str) -> str:
    """Remove a trailing host instance suffix such as ``<3>`` or ``(2)``."""
    prev = None
    out = name.strip()
    while prev != out:
        prev = out
        out = _INSTANCE_SUFFIX.sub("", out).strip()
    return out


def _stem_and_index(name: str) -> Tuple[str, str]:
    """Split ``"Boss-Extrude12"`` into ``("boss-extrude", "12")``."""
    base = strip_instance_suffix(name).strip()
    base = _WS.sub(" ", base)
    m = _TRAILING_INDEX.match(base)
    if m and m.group("stem").strip():
        return m.group("stem").strip().casefold(), m.group("num")
    return base.casefold(), ""


def is_default_name(name: str) -> bool:
    """True when ``name`` looks auto-generated by a CAD host (not user-typed).

    Empty / whitespace-only names count as default (no information).
    """
    if name is None:
        return True
    base = strip_instance_suffix(str(name)).strip()
    if not base:
        return True
    stem, _index = _stem_and_index(base)
    if stem in _STEM_SET:
        return True
    # A bare number ("12") or a bare non-alphabetic token carries no semantics.
    if not any(ch.isalpha() for ch in base):
        return True
    return False


def is_user_name(name: str) -> bool:
    """Complement of :func:`is_default_name`."""
    return not is_default_name(name)


# ---------------------------------------------------------------------------
# normalisation / tokenisation
# ---------------------------------------------------------------------------


def normalize_name(name: str, *, lower: bool = True, replace_underscore: bool = True) -> str:
    """Canonical whitespace-separated form of a raw CAD name.

    Mirrors the paper's ``LowerAndReplace_Transform`` but additionally strips
    instance suffixes and splits camelCase / digit boundaries, so that
    ``"M6_boltHead <2>"`` and ``"m6 bolt head"`` collapse to the same string.
    """
    text = strip_instance_suffix(str(name))
    text = _SEPARATORS.sub(" ", text) if replace_underscore else text
    text = _CAMEL.sub(" ", text)
    text = _ALNUM_SPLIT.sub(" ", text)
    text = _WS.sub(" ", text).strip()
    if lower:
        text = text.casefold()
    return text


def tokenize_name(name: str) -> List[str]:
    """Ordered token list of a name (no stopword removal, no stemming)."""
    norm = normalize_name(name)
    return [t for t in norm.split(" ") if t]


def name_key(name: str) -> str:
    """Order-insensitive dedup key: sorted unique tokens joined by spaces."""
    return " ".join(sorted(set(tokenize_name(name))))


def dedupe_names(names: Iterable[str]) -> List[str]:
    """Order-preserving dedup of names using :func:`normalize_name` as key."""
    seen = set()
    out: List[str] = []
    for n in names:
        key = normalize_name(n)
        if not key or key in seen:
            continue
        seen.add(key)
        out.append(n)
    return out


# ---------------------------------------------------------------------------
# corpus cleaning (paper schema)
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class CleanDocument:
    """A document after default-name removal and dedup."""

    document_id: str
    document_name: str
    document_description: str = ""
    body_names: Tuple[str, ...] = field(default_factory=tuple)
    feature_names: Tuple[str, ...] = field(default_factory=tuple)

    @property
    def num_parts(self) -> int:
        return len(self.body_names)

    def as_dict(self) -> Dict[str, object]:
        return {
            "body_names": list(self.body_names),
            "feature_names": list(self.feature_names),
            "document_name": self.document_name,
            "document_description": self.document_description,
        }


def clean_document(document_id: str, doc: Mapping[str, object]) -> CleanDocument:
    """Drop default names and dedupe, keeping the paper's document schema."""
    bodies = [str(n) for n in doc.get("body_names", []) or []]
    features = [str(n) for n in doc.get("feature_names", []) or []]
    doc_name = str(doc.get("document_name", "") or "")
    desc = str(doc.get("document_description", "") or "")
    return CleanDocument(
        document_id=str(document_id),
        document_name="" if is_default_name(doc_name) else doc_name,
        document_description=desc,
        body_names=tuple(dedupe_names(n for n in bodies if is_user_name(n))),
        feature_names=tuple(dedupe_names(n for n in features if is_user_name(n))),
    )


def clean_corpus(corpus: Mapping[str, Mapping[str, object]]) -> Dict[str, CleanDocument]:
    """Clean a whole ``{document_id: document}`` corpus, sorted by id."""
    return {
        doc_id: clean_document(doc_id, corpus[doc_id]) for doc_id in sorted(corpus)
    }


def document_features(doc: CleanDocument) -> Tuple[int, int, int, int, int]:
    """The paper's five stratification flags for a cleaned document.

    ``(has_part, two_or_more_parts, has_feature, parts_and_features,
    has_description)`` -- each 0/1.
    """
    has_part = 1 if doc.body_names else 0
    two_parts = 1 if len(doc.body_names) >= 2 else 0
    has_feature = 1 if doc.feature_names else 0
    both = 1 if (has_part and has_feature) else 0
    has_desc = 1 if len(doc.document_description.strip()) >= 2 else 0
    return (has_part, two_parts, has_feature, both, has_desc)


def name_statistics(names: Sequence[str]) -> Dict[str, object]:
    """Deterministic summary of a name list (for corpus reports)."""
    total = len(names)
    defaults = sum(1 for n in names if is_default_name(n))
    tokens = [t for n in names for t in tokenize_name(n)]
    unique_tokens = sorted(set(tokens))
    return {
        "total": total,
        "default": defaults,
        "user": total - defaults,
        "default_ratio": (defaults / total) if total else 0.0,
        "tokens": len(tokens),
        "unique_tokens": len(unique_tokens),
        "mean_tokens_per_name": (len(tokens) / total) if total else 0.0,
        "vocabulary": unique_tokens,
    }
