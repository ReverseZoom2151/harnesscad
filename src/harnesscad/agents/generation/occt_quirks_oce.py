def quirks_for_operation(operation: str) -> List[Quirk]:
    """All catalogued quirks for an operation family (e.g. ``"pcurve"``)."""
    op = operation.strip().lower()
    return [q for q in OCE_QUIRKS if q.operation == op]


def quirk_by_id(quirk_id: str) -> Optional[Quirk]:
    for q in OCE_QUIRKS:
        if q.id == quirk_id:
            return q
    return None


def operations() -> List[str]:
    """Sorted unique operation families present in the catalog."""
    return sorted({q.operation for q in OCE_QUIRKS})


