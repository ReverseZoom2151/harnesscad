def main(argv: Optional[Sequence[str]] = None) -> int:
    parser = argparse.ArgumentParser(
        description="OCCT kernel-quirk catalog mined from the OCE/OCCT "
                    "sources (reviewer tags + Mantis bug ids); LGPL-2.1 "
                    "with OCCT exception, facts-with-citation only.",
    )
    parser.add_argument("--selfcheck", action="store_true",
                        help="assert catalog integrity (schema completeness, "
                             "unique ids, citations) and exercise lookups.")
    args = parser.parse_args(list(argv) if argv is not None else None)
    if not args.selfcheck:
        parser.print_help()
        return 0

    # 1. Catalog integrity: non-empty, unique ids, every field populated,
    #    every source cites a file inside the mined repo.
    assert len(OCE_QUIRKS) >= 15, len(OCE_QUIRKS)
    ids = [q.id for q in OCE_QUIRKS]
    assert len(ids) == len(set(ids)) == 30, ids
    for q in OCE_QUIRKS:
        for f in (q.id, q.operation, q.quirk, q.trigger, q.workaround,
                  q.source):
            assert f and isinstance(f, str), q.id
        assert OCE_REPO in q.source, q.id       # citation names the repo
        assert "/src/" in q.source, q.id        # ... and a source path
        assert q.id == q.id.lower(), q.id       # kebab-case ids
        d = q.to_dict()
        assert set(d) == {"id", "operation", "quirk", "trigger",
                          "workaround", "source"}, q.id
        # ASCII-only guarantee for every field.
