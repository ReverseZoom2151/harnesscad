"""Best-of-N + verifier (HARNESS_BLUEPRINT.md sec.4).

The highest-ROI reliability escalation in the blueprint: instead of trusting one
sampled op-plan, draw N of them and let the *deterministic verifier* choose. Since
CAD is a verifiable-reward domain, the verifier is simultaneously reward, eval, and
selector — so Best-of-N is nearly free signal.

    P(success) = 1 - (1 - p)^N

where `p` is the single-shot success probability. Each candidate is generated by a
varied planner call (a per-index seed/nonce folded into the brief — `Math.random`
is unavailable and we want determinism) and applied through a FRESH
`HarnessSession` (via the injected `session_factory`) so candidates never share
state. The winner is the candidate whose `ApplyOpsResult` scores highest.

Candidates run sequentially here (simple + deterministic for tests), but each is
independent and side-effect-isolated, so a caller can trivially parallelize the
generate+apply of each index (e.g. a thread/process pool) and keep this exact
selection logic.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Callable, List, Optional

from harnesscad.core.cisp.ops import Op
from harnesscad.core.cisp.protocol import ApplyOpsResult
from harnesscad.eval.verifiers.verify import Diagnostic, Severity


@dataclass
class Candidate:
    """One Best-of-N attempt: the seeded brief, its plan, and its applied result."""

    index: int
    brief: str
    ops: Optional[List[Op]] = None
    result: ApplyOpsResult = None  # type: ignore[assignment]
    error: Optional[str] = None    # set when planning raised (bad model output)

    @property
    def ok(self) -> bool:
        return bool(self.result and self.result.ok)


@dataclass
class BestOfNResult:
    """All candidates plus the selected winner."""

    candidates: List[Candidate] = field(default_factory=list)
    winner: Optional[Candidate] = None
    winner_index: int = -1
    n: int = 0

    @property
    def ok(self) -> bool:
        return bool(self.winner and self.winner.ok)


def default_scorer(result: ApplyOpsResult):
    """Prefer verified, then fewer diagnostics, then more ops applied.

    Returns a sortable tuple (higher is better); ties fall back to candidate order
    at the call site (a stable max keeps the first/lowest-seed winner).
    """
    ok = 1 if result.ok else 0
    n_diags = len(result.diagnostics)
    return (ok, -n_diags, result.applied)


def _seeded_brief(brief: str, index: int) -> str:
    """Fold a deterministic per-candidate seed into the brief.

    Math.random is unavailable and we want reproducible runs, so diversity comes
    from an explicit nonce/temperature hint the planner can read. Candidate 0 is
    the unmodified brief (the natural single-shot); 1..N-1 carry a seed line.
    """
    if index == 0:
        return brief
    return (
        f"{brief}\n\n"
        f"[sampling hint: candidate #{index} of a best-of-N draw; explore an "
        f"alternative construction. seed={index}]"
    )


def best_of_n(
    planner,
    session_factory: Callable[[], object],
    brief: str,
    n: int,
    scorer: Optional[Callable[[ApplyOpsResult], object]] = None,
) -> BestOfNResult:
    """Draw `n` candidate op-plans and return them plus the verifier-selected winner.

    Args:
        planner: anything with ``plan(brief, state_summary=None, diagnostics=None)
            -> list[Op]`` (e.g. ``agent.planner.Planner``). Called once per candidate
            with a seeded brief so the draws differ.
        session_factory: zero-arg factory returning a FRESH ``HarnessSession`` (with
            its own backend). Called once per candidate — candidates never share state.
        brief: the natural-language design brief.
        n: number of candidates to draw (``n >= 1``).
        scorer: ``scorer(result) -> sortable``; higher wins. Defaults to
            ``default_scorer`` (ok, then fewer diagnostics, then more applied).

    Returns:
        ``BestOfNResult`` with every ``Candidate`` and the winner.
    """
    if n < 1:
        raise ValueError(f"n must be >= 1 (got {n})")
    score = scorer or default_scorer

    candidates: List[Candidate] = []
    for i in range(n):
        seeded = _seeded_brief(brief, i)
        cand = Candidate(index=i, brief=seeded)
        session = session_factory()
        try:
            ops = planner.plan(seeded, state_summary=session.summary())
            cand.ops = ops
            cand.result = session.apply_ops(ops)
        except Exception as exc:  # planner produced unusable output for this draw
            cand.error = f"{type(exc).__name__}: {exc}"
            cand.ops = None
            cand.result = ApplyOpsResult(
                ok=False,
                applied=0,
                digest=session.digest(),
                diagnostics=[Diagnostic(
                    Severity.ERROR, "plan-error",
                    f"candidate {i} failed to plan: {cand.error}")],
                rejected=None,
            )
        candidates.append(cand)

    # Stable max: iterate in index order and keep strictly-better replacements, so
    # equal scores resolve to the earliest (lowest-seed) candidate.
    winner: Optional[Candidate] = None
    best_score = None
    for cand in candidates:
        s = score(cand.result)
        if winner is None or s > best_score:
            winner, best_score = cand, s

    return BestOfNResult(
        candidates=candidates,
        winner=winner,
        winner_index=winner.index if winner else -1,
        n=n,
    )
